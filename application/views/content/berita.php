	<p><a href="#">Lowongan Pekerjaan Web Programmer (WB) dan Database Administrator(DBA)</a></p>

	<p>8 Februari 2019</p>

	 <strong>WEB PROGRAMMER (WB)</strong>
	<p>Kualifikasi:</p>
	<p>1. Pendidikan Min D3 Teknik Informatika/Elektro/Ilmu Komputer</p>
	<p>2. Menguasai dan familiar dengan PHP, MySQL, CSS, HTML dan JavaScript</p>
	<p>3. Menguasai pemgrograman Python sebagai nilai tambah</p>
	<p>4. Mampu bekerja dalam tim dan jadwal yang ketat</p>
	<p>5. Memiliki kemampuan komunikasi yang baik</p>
	<p>6. Memiliki kemauan belajar</p>
	<p>7. Pengalaman dalam mengembangkan aplikasi berbasis web menjadi nilai tambah</p>
	<p>&nbsp;</p>
	<p><strong>DATABASE ADMINISTRATOR (DBA)</strong></p>
	<p>Kualifikasi:</p>
	<p>Pendidikan Min. S1 Teknik Informatika/Elektro/Ilmu Komputer</p>
	<p>Menguasai dan familiar menggunakan functions, triggeres, views, queries dan replication pada PostgreSQL</p>
	<p>Memiliki ketrampilan pemecahan masalah yang baik</p>
	<p>Mampu bekerja dalam tim dan jadwal yang ketat</p>
	<p>Memiliki kemampuan komunikasi yang baik</p>
	<p>&nbsp;</p>
	<p>Kirim CV + Lamaran Lengkap (Ijazah, Transkrip NIlai, KTP, Sertifikat Pendukung, Portofolio, Foto berwarna) ke email <strong>hcm@gi.co.id</strong>, dengan mencantumkan kode jabatan pada subject email.</p>
	<p>Paling lambat 8 Maret 2019</p>	
